function changeFontsize(target){
	if ( target == "small" ){
		document.getElementById("section").style.fontSize = "10px";
	}
	if ( target == "medium" ){
		document.getElementById("section").style.fontSize = "14px";
	}
	if ( target == "large" ){
		document.getElementById("section").style.fontSize = "16px";
	}
}

